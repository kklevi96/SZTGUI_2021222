    public partial class MainWindow : Window
    {
        public MainWindow()
        {
            InitializeComponent();
        }

        private void Window_SizeChanged(object sender, SizeChangedEventArgs e)
        {
            if (grid.ActualWidth > 0 && grid.ActualHeight > 0)
            {
                display.Resize(
                    new Size() 
                    {
                        Width = grid.ActualWidth,
                        Height = grid.ActualHeight
                    });

            }

        }
    }
